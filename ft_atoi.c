#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	count_minus;
	int	result;

	i = 0;
	count_minus = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			count_minus *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * count_minus);
}
